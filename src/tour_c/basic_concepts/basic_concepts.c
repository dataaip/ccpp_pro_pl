/*
### 基本概念

本节提供了描述 C 编程语言时使用的特定术语和概念的定义。

C 程序是包含声明的文本文件（通常是头文件和源文件）的序列。它们经过转换成为可执行程序，该程序在 OS 调用其 main 函数时执行（除非它本身是 OS 或其他独立程序，在这种情况下，入口点是实现定义的）。

C 程序中的某些单词具有特殊含义，它们是关键字。其他 字符 可以用作标识符，可用于标识对象、函数、结构、联合或枚举标记、其成员、typedef 名称、标签或宏。

每个标识符（宏除外）仅在程序中称为其范围的部分内有效，并且属于四种名称空间之一。某些标识符具有链接，这使得它们在出现在不同的范围或翻译单元中时引用相同的实体。

函数的定义包括语句和声明的序列，其中一些包括表达式，这些表达式指定程序要执行的计算。

声明和表达式创建、销毁、访问和作对象。C 中的每个对象、函数和表达式都与一个类型相关联。
*/

/*
我们被要求对C语言的基本概念进行深入完整的解释，并举例说明和总结。根据提供的文本，我们需要涵盖以下关键点：

 1. C程序的结构：文本文件（头文件和源文件）序列，经过转换成为可执行程序，从main函数（或实现定义的入口点）开始执行。
 2. 关键字和标识符：关键字有特殊含义，标识符用于命名各种实体（对象、函数、结构等）。
 3. 作用域和名字空间：标识符的作用域和名字空间（四种）。
 4. 链接：某些标识符具有链接属性，使得它们在不同作用域或翻译单元中引用同一实体。
 5. 函数定义：包括语句和声明，其中可能包含表达式。
 6. 对象和类型：声明和表达式操作对象，每个对象、函数和表达式都有类型。

 我们将逐一解释这些概念，并给出例子，最后总结。

 注意：由于问题要求深入完整，我们将尽量详细，但也要注意篇幅。

 *************************************
 1. C程序结构
    C程序由多个文本文件组成，通常包括源文件（.c）和头文件（.h）。这些文件经过预处理、编译、链接等步骤生成可执行文件。
    当操作系统运行这个可执行文件时，会调用main函数（除非是独立环境，如操作系统内核，则入口点由实现定义）。

    例子：
        // hello.c
        #include <stdio.h>  // 头文件

        int main(void) {
            printf("Hello, world!\n");
            return 0;
        }

    编译并运行：
        gcc hello.c -o hello
        ./hello

 *************************************
 2. 关键字和标识符
    - 关键字：C语言保留的具有特殊含义的单词，如int, if, for等。不能用作标识符。
    - 标识符：由程序员定义的名称，用于命名变量、函数、结构等。标识符由字母、数字和下划线组成，且不能以数字开头。

    例子：
        int var;        // var 是标识符
        void func(void); // func 是标识符

    注意：标识符不能是关键字。

 *************************************
 3. 作用域和名字空间
    - 作用域：标识符的有效范围。在C语言中，作用域可以是块作用域（在{}内）、文件作用域（在所有函数外）等。
    - 名字空间：C语言有四种名字空间，用于避免命名冲突：
        a) 标签名（label names）：goto语句使用的标签。
        b) 结构、联合和枚举标记（struct, union, enum tags）。
        c) 结构或联合的成员（每个结构或联合独立）。
        d) 其他标识符（变量、函数、typedef名、枚举常量等）。

    因此，同一个名字在不同名字空间中可以被重复使用。

    例子：
        struct tag { int tag; };  // 这里的'tag'在标记名字空间，而成员'tag'在成员名字空间（但同一个结构内成员名字必须唯一）
        int tag; // 在普通标识符名字空间，与上面的标记和成员不冲突。

        void func() {
            goto tag;   // 标签名字空间
            tag:        // 标签
            int tag = 1; // 普通标识符，与标签、结构标记、结构成员都不冲突（作用域不同可能也会影响，但名字空间不同）
        }

    注意：虽然名字空间不同，但为了避免混淆，通常不会这样用。

 *************************************
 4. 链接
    链接属性决定了标识符在不同作用域或翻译单元中是否指向同一个实体。
    - 外部链接（external linkage）：可以在整个程序（多个翻译单元）中引用同一个实体。
    - 内部链接（internal linkage）：只在当前翻译单元内引用同一个实体。
    - 无链接（no linkage）：只在当前作用域内有效（如局部变量）。

    例子：
        // file1.c
        int global_var;         // 外部链接（默认），可以在其他文件中用extern引用
        static int file_var;     // 内部链接，只在file1.c中可见

        // file2.c
        extern int global_var;   // 声明，链接到file1.c中的global_var
        // extern int file_var;  // 错误，file_var在file1.c中是static，不可见

        void func() {
            int local_var;       // 无链接，只在函数内部
        }

 *************************************
 5. 函数定义
    函数定义包括返回类型、函数名、参数列表和函数体。函数体由一系列声明和语句组成。

    例子：
        int add(int a, int b) { // 函数定义
            int sum = a + b;    // 声明并初始化
            return sum;         // 语句
        }

    函数体中的表达式（如a+b）用于计算。

 *************************************
 6. 对象和类型
    C语言中，对象（这里指存储区域）具有类型。每个表达式、函数也都有类型。
    - 类型决定了对象的大小、表示方式以及能进行的操作。
    - 声明创建对象（如变量声明），表达式访问和操作对象。

    例子：
        int x = 10;      // 声明一个int类型的对象x
        double y = 3.14;  // 声明一个double类型的对象y
        x = x + 5;        // 表达式：访问x，计算x+5，再赋值给x

 *************************************
 总结：
    C程序由多个文件组成，经过编译链接后执行，从main函数开始。
    关键字是保留字，标识符是程序员定义的名称，属于四种名字空间之一，作用域决定其可见范围。
    链接属性决定了标识符在不同作用域或文件中的关联方式。
    函数由语句和声明组成，其中表达式用于计算。
    每个实体都有类型，类型决定了数据的解释和操作。

 通过以上解释和例子，我们详细介绍了C语言的基本概念。这些概念是理解C语言的基础。
*/

